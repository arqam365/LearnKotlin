fun main() {
    val maxIntegerValue: Int = Int.MAX_VALUE
    val minIntegerValue: Int = Int.MIN_VALUE
    println("Int maximum value is : $maxIntegerValue")
    println("Int minimum value is : $minIntegerValue \n")

    val maxByteValue: Byte = Byte.MAX_VALUE
    val minByteValue: Byte = Byte.MIN_VALUE

    println("Byte maximum value is : $maxByteValue")
    println("Byte minimum value is : $minByteValue \n")

    val maxShortValue: Short = Short.MAX_VALUE
    val minShortValue: Short = Short.MIN_VALUE

    println("Short maximum value is : $maxShortValue")
    println("Short minimum value is : $minShortValue \n")

    val maxLongValue: Long = Long.MAX_VALUE
    val minLongValue: Long = Long.MIN_VALUE

    println("Long maximum value is : $maxLongValue")
    println("Long minimum value is : $minLongValue \n")
}